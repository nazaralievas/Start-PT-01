# задание про выборы:
# ученики выбирают президента школы
# напишите программу, чтобы каждый ученик мог вводить в программу определённое число:
# 1 - если голосуют за Нурай
# 2 - если за Айку
# 3 - за Меку
# в конце голосования в терминал вводят ноль
# 0 - для завершения голосования
# нужно вывести сколько голосов получил каждый кандидат
# изначально у кандидатов по ноль голосов
nurai = 0
aika = 0
meka = 0

golos = int(input('Введите 1 если вы за Нурай. 2 если за Айку. 3 если за Меку: '))
while golos != 0:
    if golos == 1: # если пользователь ввёл 1
        nurai += 1 # Нурай получает плюс 1 голос
    elif golos == 2: # если пользователь ввёл 2
        aika += 1 # Айка получает плюс 1 голос
    elif golos == 3: # если пользователь ввёл 3
        meka += 1 # Мека получает плюс 1 голос
    else: # если пользователь ввёл что-то кроме 1, 2, 3, то выводится такое сообщение:
        print('Такого кандидата нет.')

    golos = int(input('Введите 1 если вы за Нурай. 2 если за Айку. 3 если за Меку: '))


# после завершения голосования (когда ввели ноль) выводим результаты голосования:
print(f'За Нурай проголосовало: {nurai} учеников')
print(f'За Айку проголосовало: {aika} учеников')
print(f'За Меку проголосовало: {meka} учеников')


