# в среднем от 2 до 3.5 других, если не принимать никаких мер.

infected = 1
growth_rate = 2.5  # R₀
days = 10

day = 0
while day < days:
    print(f"День {day}: {infected:.0f} заражённых")
    infected *= growth_rate
    day += 1