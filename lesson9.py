# задание, которое делали на уроке: нужно было посчитать сумму чётных и нечётных чисел от 1 до 100 включительно
# сначала создадим переменные, куда будем складывать чётные и нечётные числа
summa_chet = 0
summa_nechet = 0

for i in range(1, 101): # берём числа от 1 до 100
    if i % 2 == 0: # если остаток от деления числа на 2 равняется нулю (т.е. если число чётное)
        summa_chet = summa_chet + i # прибавляем его к summa_chet
    else: # если нет
        summa_nechet += i # прибавляем его к summa_nechet

print(summa_chet)
print(summa_nechet)


# давайте теперь посчитаем количество чётных чисел
kol_chet = 0 # создадим переменную-счётчик
for i in range(1, 101):
    if i % 2 == 0: # если число чётное
        kol_chet = kol_chet + 1 # прибавляем к счётчику не само число, а 1
    else: # если число нечётное
        continue # просто переходим к следующему числу

print(kol_chet)


