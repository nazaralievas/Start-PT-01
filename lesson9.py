num = int(input("Введите число: "))
print(num * '*') # верхняя линия
# так как первую и последнюю линии мы уже нарисовали, осталось нарисовать остальные
# поэтому в цикле будет num - 2 повторений
for i in range(num - 2): 
    print("*" + " " * (num - 2) + "*")

print(num * '*')  # нижняя линия


