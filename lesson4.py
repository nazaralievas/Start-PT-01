# Напишите программу, которая принимает от пользователя количество минут
# и преобразует указанное количество минут в часы и минуты.
# Например, пользователь вводит: 80
# В терминале нужно вывести:
# 80 минут — это 1 час и 20 минут
data = int(input('Введите количество минут: '))
hours = data // 60
minutes = data % 60
print(f'{data} минут это {hours} час(а/ов) и {minutes} минут')

# познакомимся с условным оператором if, который поможет нам поставить проверку:
# если пользователь ввёл число меньше нуля, то программа выводит сообщение
# "Введите положительное число!" и не выполняет расчёты
# в противном случае программа считает сколько во введённых минутах часов и минут и выводит ответ

data = int(input('Введите количество минут: ')) # запросим у пользователя количество минут
if data < 0: # если введённое число меньше нуля (то есть отрицательное число)
    print('Введите положительное число!') # выводим это сообщение
else: # во всех остальных случаях
    hours = data // 60 # производим расчёт
    minutes = data % 60
    print(f'{data} минут это {hours} час(а/ов) и {minutes} минут') # и выводим ответ

# но что произойдёт, если пользователь введёт ноль?
# программа выведет "0 минут это 0 час(а/ов) и 0 минут"
# такой ответ нас не устраивает
# давайте в условие if добавим знак =
# это позволит отловить вариант, если пользователь ввёл ноль
data = int(input('Введите количество минут: ')) # запросим у пользователя количество минут
if data <= 0: # если введённое число меньше нуля (то есть отрицательное число)
    print('Введите положительное число!') # выводим это сообщение
else: # во всех остальных случаях
    hours = data // 60 # производим расчёт
    minutes = data % 60
    print(f'{data} минут это {hours} час(а/ов) и {minutes} минут') # и выводим ответ



