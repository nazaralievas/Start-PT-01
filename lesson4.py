# задание 2
# у пользователя на счету 10000 долларов (balance = 10000)
# пользователь вводит сумму, которую хочет снять
# если у него достаточно денег, то сумма отнимается от balance
# и показывается сообщение "У вас осталось: ___ долларов"
# если денег недостаточно, выводится сообщение "Недостаточно денег на счету"
# при проверке баланса, то есть print(balance) должна выводиться актуальная инф-я о количестве денег
balance = 10000
snyat = int(input('Какую сумму хотите снять?: '))
if snyat > balance:
    print('Недостаточно денег на счету!')
else:
    balance -=snyat # от переменной balance отнимаем сумму, которую сняли
    print('У вас осталось: ', balance)


