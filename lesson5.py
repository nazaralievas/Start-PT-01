# Итак, разберём домашнее задание: нужно было написать программу,
# которая запрашивает у посетителя его рост. Если рост больше 2 метров или меньше 140 см
# то мы с ним прощаемся. Если рост в пределах допустимого, мы запрашиваем его вес
# если вес меньше 150 и больше 40 кг, то он допускается на аттракцион
# если нет, то мы с ним прощаемся
rost = int(input('какой у вас рост? '))
if rost > 200 or rost < 140: # если рост больше 2 метров или меньше 140 см
    print('Вы не подходите по росту...')
else:
    ves = int(input('какой у вас вес? '))
    if 150 > ves > 40: # ves < 150 and ves > 40 то есть если вес меньше 150 и больше 40 кг
        print('Добро пожаловать!')
    else:
        print('Вы не проходите по весу!')


