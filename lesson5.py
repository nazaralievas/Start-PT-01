# Пользователь вводит количество собранных очков
# если очков от 1 до 9: выигрывает грамоту
# если очков от 10 до 19: выигрывает мяч
# если очков от 20 до 29: бадминтон
# если от 30 до 40: наушники
# если вводит 0, <0 или больше 40, программа выводит соответствующее сообщение о некорректности ввода
points = int(input('Сколько у вас очков? '))
if 1 <= points <= 9:
    print('Вы выиграли грамоту!')
elif 10 <= points <= 19:
    print('Вы выиграли мяч!')
elif 20 <= points <= 29:
    print('Вы выиграли бадминтон!')
elif 30 <= points <= 40:
    print('Вы выиграли наушники!')
else:
    print('Некорректный ввод.')

# прогрессивный налог на прибыль: чем больше прибыль, тем выше налог
# напишите программу, в которую бизнесмен вводит свою прибыль за месяц
# и программа высчитывает сколько налогов он заплатит от введённой суммы
# если прибыль до 5 тыс, налог 10%
# если от 5тыс до 50тыс, налог 20%
# если больше 50тыс, налог 35%
# нужно вывести сообщение "В этом месяце вы заплатите ___ в виде налогов."
money = int(input('Сколько у вас очков? '))
if 0 <= money <= 5000:
    tax = money / 100 * 10
    print(f'В этом месяце вы заплатите {tax} в виде налогов.')
elif money <= 50000:
    tax = money / 100 * 20
    print(f'В этом месяце вы заплатите {tax} в виде налогов.')
elif money > 50000:
    tax = money / 100 * 35
    print(f'В этом месяце вы заплатите {tax} в виде налогов.')
else:
    print('Некорректный ввод.')