# задание №4
# сделайте так, чтобы третьим по счёту был студент Терёшин:
students.insert(2, "Терёшин")
print(students) # ['Баруах', 'Каныметов', 'Терёшин', 'Акназарова', 'Соловарев', 'Абдуллаев', 'Акимбаев', 'Кубатбеков']
# теперь Терёшин стоит третий (под вторым индексом)
# а Акназарова сместилась назад

# что если нам нужно поставить на место студента Соловарева другого студента по фамилии Исмаилов?
# нам нужно узнать индекс элемента "Соловарев"
# для этого мы можем вывести в столбик числа 0, 1, 2, 3, 4....
# и рядом элементы списка students
# это можно сделать с помощью функции enumerate:
for index, student in enumerate(students):
    print(f'{index} - {student}')

# и вот какой вывод мы получили:
# 0 - Баруах
# 1 - Каныметов
# 2 - Терёшин
# 3 - Акназарова
# 4 - Соловарев
# 5 - Абдуллаев
# 6 - Акимбаев
# 7 - Кубатбеков

# то есть это два цикла for выведенные одновременно. Поэтому нам понадобилось 2 переменных:
# в переменной index хранятся числа от 0 и дальше по возрастанию
# в переменной student хранятся элементы списка

# итак, мы узнали индекс элемента Соловарев
# теперь на этом индексе у нас будет студент Исмаилов, а Соловарев и осталльные сместятся назад
students.insert(4, "Исмаилов")
print(students)
# вот результат:
# ['Баруах', 'Каныметов', 'Терёшин', 'Акназарова', 'Исмаилов', 'Соловарев', 'Абдуллаев', 'Акимбаев', 'Кубатбеков']


# но для того, чтобы узнать индекс нужного элемента есть встроенный метод index
i = students.index('Соловарев') # 5
# теперь по этому индексы мы можем вставить новый элемент, например нового студента с фамилией "Ли"
students.insert(i, "Ли")
print(students)
# результат:
# ['Баруах', 'Каныметов', 'Терёшин', 'Акназарова', 'Исмаилов', 'Ли', 'Соловарев', 'Абдуллаев', 'Акимбаев', 'Кубатбеков']
# остальные студенты сдвинулись назад


# а что если мы хотим не просто сдвинуть элемент, а ЗАМЕНИТЬ ЕГО
# что если мы хотим заменить элемент 'Каныметов' на 'Бошкоев'
# для этого указываем индекс элемента и пишем новое значение:
students[1] = 'Бошкоев'
print(students)
# теперь у нас на месте 'Каныметов' стоит 'Бошкоев'
# 'Каныметов' в списке нет вообще:
# ['Баруах', 'Бошкоев', 'Терёшин', 'Акназарова', 'Исмаилов', 'Ли', 'Соловарев', 'Абдуллаев', 'Акимбаев', 'Кубатбеков']
