# разберём домашку
# нужно было из списка students вывести фамилии, начинающийеся с буквы А
students = ["Каныметов", "Акназарова", "Соловарев",
            "Абдуллаев", "Акимбаев", "Кубатбеков"]

for s in students:
    if s[0] == "А":
        print(s)
# Акназарова
# Абдуллаев
# Акимбаев


# сегодня научимся добавлять новый элемент в список
# для добавления используется метод append
# например, мы хотим добавить нового студента с фамилией Баруах в наш список students:
students.append("Баруах")
print(students) # ['Каныметов', 'Акназарова', 'Соловарев', 'Абдуллаев', 'Акимбаев', 'Кубатбеков', 'Баруах']
# "Баруах" добавился в конец списка

